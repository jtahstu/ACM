#include<iostream>
#include<cstdio>
using namespace std;
int main24() {
	int count=0;
	for (int i = 20; i >= 0; i--) { //五分
		for (int j = 0; j <= 50; j++) { //二分
			if((i*5+j*2)>100)break;//优化一下
			for (int k = 0; k <= 100; k++) //一分
					{
				if((i*5+j*2+k)==100){
					count++;
					cout<<count<<":1*"<<k<<"+2*"<<j<<"+5*"<<i<<" ";
					if(count%8==0)cout<<endl;
				}
			}
		}
	}
	return 0;
}

