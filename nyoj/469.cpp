#include<stdio.h>
int main1()
{
    int n, i;
    long a[58], b[58]; //a[i]的值代表以i结尾的排列的数量，b[i]的值代表不以i结尾的排列的数量。
    a[1] = 1;
    b[1] = 0;
    a[2] = 1;
    b[2] = 0;
    a[3] = 1;
    b[3] = 1;
    for (i = 4; i < 56; i++)
    {
        a[i] = a[i - 1] + a[i - 3]; //以i结尾的排列数等于以i-1结尾的加上i-3结尾的总数，因为其中不以i-1结尾的i-2已确定。
        b[i] = a[i - 2] + b[i - 1];
    }
    while ((scanf("%d", &n)==1) && n)
    {
        printf("%d,", a[n] + b[n]);
    }
    return 0;
}
